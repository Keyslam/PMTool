        <h4>Spelverloop</h4>
        <p>
            Een zogenaamde dealer button geeft aan wie de dealer is. Het inzetten begint met een small blind en een big
            blind.
            Dat zijn inzetten die spelers verplicht moeten doen. Vervolgens zet iedereen in de gebruikelijke volgorde
            in.
            Als je
            op internet speelt wordt het delen voor je gedaan en de dealer button getoond aan tafel.
        </p>

        <h5>De blinds</h5>
        <p>
            Voordat we echt beginnen te spelen is de eerste speler links van de dealer verplicht om een vooraf
            afgesproken
            bedrag in te zetten (small blind), waarna de speler links van deze small blind op zijn beurt de big blind
            zet,
            die
            in de meeste gevallen twee keer zo hoog is als de small blind. De hoogte hangt af van de gespeelde limiet.
            Bij
            online poker gaat het allemaal automatisch.
        </p>

        <h5>Eerste inzetronde</h5>
        <p>
            De speler aan de linkerkant van de big blind is als eerste aan de beurt. Hij heeft drie opties: call, raise
            of
            fold.
            Bij call zet de speler het zelfde bedrag in als de big blind, bij raise zet deze pokerspeler een extra
            bedrag
            in.
            Bij fold is deze ronde voor hem ten einde, je speelt niet meer mee om de pot, maar het kost hem ook geen
            inzet.
            Bijkomende optie is re-raise, een extra verhoging na een raise.
        </p>

        <h5>Nogmaals de drie opties:</h5>
        <p>
            <b>Fold.</b> Je kaarten zijn te slecht om te spelen. Je gooit ze weg.<br>
            <b>Call.</b> Je gaat mee met de inzet van de big blind en legt hetzelfde aantal fiches in.<br>
            <b> Raise.</b> Je verhoogt de inzet en legt dus meer in dan de big blind.<br>
        </p>

        <h5>De Flop en de 2e inzetronde</h5>
        <p>
            Er komen 3 kaarten op tafel die voor iedereen zichtbaar zijn. Deze drie kaarten vormen de flop en elke
            speler
            gebruikt ze om samen met zâ€™n handkaarten een combinatie van vijf kaarten te maken. Behalve call, raise of
            re-raise
            kun je ook checken. Je mag gratis doorspelen tot de volgende beurt. Als er een inzet voor je is gedaan, kun
            je
            callen voor dat bedrag of het bedrag verhogen (raise).
        </p>

        <h5>De Turn en de 3e inzetronde</h5>
        <p>
            Na de tweede inzetronde, wordt de volgende kaart open gelegd. Dit is de turn kaart. Deze kaart is weer een
            aanvulling op de handkaarten van de spelers om een pokerhand van vijf kaarten te maken. Spelers kunnen weer
            checken,
            betten, callen, raisen en natuurlijk folden.
        </p>

        <h5>De River en de laatste inzetronde</h5>
        <p>
            Na de derde inzetronde wordt de vijfde en laatste gemeenschappelijke kaart open gelegd. Dit is de river
            kaart.
            Nu
            volgt de laatste inzetronde. Deze gaat vooraf aan de showdown. Dat is het moment dat iedereen die nog in het
            spel
            zit de kaarten moeten openleggen. Winnaar is degene met de hoogste kaart, hij krijgt de pot, bij gelijke
            kaarten
            wordt er gedeeld.
        </p>
