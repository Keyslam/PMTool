        <h4>De kaartcombinaties</h4>
        <p>
            Onderdeel van poker is begrip van de handranking<br>
            <br>
            Dit is een soort van puntentelling om te weten of je een sterke kaart hebt. Iedere speler wil graag de
            hoogste kaart combinatie in een hand van vijf kaarten, we noemen hier de volgorde van hoog naar laag. Wie
            aan het einde van een ronde nog in het spel zit en de beste pokerhand heeft is winnaar en krijgt de pot die
            op tafel ligt.<br>
            <br>
            <b>Royal Flush:</b> aas, koning, vrouw, boer, 10 van gelijk teken is de hoogst mogelijke pokerhand.<br>
            <b>Straight Flush:</b> Vijf opeenvolgende kaarten van gelijke teken. De aas kan zowel voor de twee als na de
            koning.
            Bij meerdere straight flush-handen beslist de hoogste kaart.<br>
            <b>Four of a kind:</b> Vier gelijkwaardige kaarten in dit geval vier boeren. Een four of a kind-hand is in
            bijna
            alle
            gevallen de winnaar.<br>
            <b>Full house:</b> Combinatie van three of a kind en een one pair. Soms komt er in dezelfde ronde twee of
            meer
            keer
            full house op tafel. Dan beslist de hoogste Three of a Kind, is deze gelijk dan is het pair beslissend.<br>
            <b>Flush:</b> Vijf willekeurige kaarten van gelijke teken. Bij twee of meer flush-handen beslist de hoogste
            kaart,
            daarna de volgende in de reeks.<br>
            <b>Straight:</b> Opeenvolgende kaarten van verschillende tekens. De aas kan voor de twee en na de koning. De
            laagst
            mogelijke straat is a-2-3-4-5, de hoogste is 10-j-v-k-a. Bij meerdere straight-handen beslist de hoogste
            kaart.<br>
            <b>Three of a kind:</b> Drie gelijkwaardige kaarten, in dit geval drie koningen. Deze hand is vaak genoeg om
            de
            pot te
            winnen.<br>
            <b>Two pair:</b> Verschillende paren. Bij meer two pair-handen is de waarde van het hoogste paar beslissend.
            Is
            die
            gelijk, dan beslist de waarde van het volgende paar of van de vijfde kaart.<br>
            <b>One pair:</b> Soms is "een paartje" genoeg om de pot te winnen. Bij gelijke combinaties beslist de hoogte
            van
            de
            volgende kaart.<br>
            <b>High card:</b> Vijf verschillende kaarten zonder een combinatie. Eerst kijk je natuurlijk wie de hoogste
            kaart
            heeft. Is die gelijk dan beslist de tweede kaart, is die ook gelijk dan gaat het om de waarde van de
            volgende.
        </p>
